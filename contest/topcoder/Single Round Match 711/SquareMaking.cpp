using namespace std;

class SquareMaking {
    public:
    int getMinimalPrice(int a, int b, int c, int d) {
        int miv = min(min(a, b), min(c, d));
        int mav = max(max(a, b), max(c, d));
        int mi = INF;
        for(int i = miv; i <= mav; ++i) {
            int x = abs(a-i) + abs(b-i) + abs(c-i) + abs(d-i);
            if(x < mi) mi = x;
        }
        return mi;
    }
};

