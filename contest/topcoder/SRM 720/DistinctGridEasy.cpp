using namespace std;

class DistinctGridEasy {
    public:
        int g[55];
    string checkGrid(int n, int k, vector<int> grid) {

        for(int i = 0; i < n; ++i) {
            zero(g);
            int t = 0;
            for(int j  = 0; j < n; ++j) {
                if(!g[grid[i*n+j]]) ++t;
                g[grid[i*n+j]] = 1;
            }
            if(t != k) return "Bad";
        }
        for(int i = 0; i < n; ++i) {
            zero(g);
            int t = 0;
            for(int j  = 0; j < n; ++j) {
                if(!g[grid[i+j*n]]) ++t;
                g[grid[i+j*n]] = 1;
            }
            if(t != k) return "Bad";
        }
        return "Good";
    }
};

