using namespace std;

class ForwardMancala {
    public:
    vector<int> findMoves(vector<int> start) {
        int n = sz(start);
        vector<int > pick;
        pick.clear();
        bool flag = 0;
        while(!flag) {
            int x = n-1, y;
            for( ; x ; --x) if(start[x]) break;
            pick.pb(x);
            y = start[x];
            start[x] = 0;
            while(y--) {
                start[(++x)%n]++;
            }
//for(auto& u : start) printf("%d ", u); puts("");
            flag = 1;
            for(int i = n-1; i ; --i) if(start[i]) {
                flag = 0;
                break;
            }
        }
        return pick;
    }
};

