const int M = 1e9+7;

class SuperSubset {
    public:
    int solve(vector<int> A, int Y) {
        int n = A.size(), f = 0;
        vector<vector<int>> dp(2, vector<int>(Y+1, 0));
        dp[0][0] = 1;
        for (int i = 0; i < n; ++i) {
            for (int j = 0; j <= Y; ++j){
                dp[f^1][j] = (dp[f][j] + dp[f][j]) % M;
            }
            for (int j = 0; j <= Y; ++j) {
                int x = A[i] + j;
                if (x <= Y) dp[f^1][x] = (dp[f^1][x] + dp[f][j]) % M;
            }
            f ^= 1;
        }
        return dp[f][Y];
        
    }
};
 
